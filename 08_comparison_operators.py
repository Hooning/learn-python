# Chained comparison operators
x = 5
if 0 < x < 10:
    print("x is between 0 and 10")

# Comparison operators
# == : equal to
# != : not equal to
# > : greater than
# < : less than
# >= : greater than or equal to
# <= : less than or equal to
print(5 == 5)  # True
print(5 != 5)  # False
print(5 > 3)  # True
print(5 < 3)  # False
print(5 >= 5)  # True
print(5 <= 4)  # False
